// Jump to next map square, either in x-direction, or in y-direction
// and check if ray has hit a wall, if so give game->hit the value
// of 1.
int	perform_dda(t_game *game)
{
	while (game->hit == 0)
	{
		if (game->side_dist_x < game->side_dist_y)
		{
			game->side_dist_x += game->delta_dist_x;
			game->map_x += game->step_x;
			game->side = 0;
		}
		else
		{
			game->side_dist_y += game->delta_dist_y;
			game->map_y += game->step_y;
			game->side = 1;
		}
		if (game->map_tab[game->map_y][game->map_x] == 1)
			game->hit = 1;
	}
	return (0);
}
